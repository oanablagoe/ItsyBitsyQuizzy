import {
  Component,
  OnInit,
  OnDestroy,
  ViewChild
} from '@angular/core';
import {NgForm} from '@angular/forms';
import {Subscription} from 'rxjs/Subscription';

import {User} from '../../shared/user.model';
import {UserListService} from '../user-list.service';

@Component({
  selector: 'app-user-edit',
  templateUrl: './user-edit.component.html',
  styleUrls: ['./user-edit.component.css']
})
export class UserEditComponent implements OnInit, OnDestroy {
  @ViewChild('f') slForm: NgForm;
  subscription: Subscription;
  editMode = false;
  editedItemIndex: number;
  editedItem: User;

  constructor(private slService: UserListService) {
  }

  ngOnInit() {
    this.subscription = this.slService.startedEditing
      .subscribe(
        (index: number) => {
          this.editedItemIndex = index;
          this.editMode = true;
          this.editedItem = this.slService.getUser(index);
          this.slForm.setValue({
            name: this.editedItem.name
          });
        }
      );
  }

  onSubmit(form: NgForm) {
    this.editMode = false;
    form.reset();
  }

  onClear() {
    this.slForm.reset();
    this.editMode = false;
  }

  onDelete() {
    this.slService.deleteUser(this.editedItemIndex);
    this.onClear();
  }

  onEdit() {
    const name = (<HTMLInputElement>document.getElementById('name')).value;
    this.slService.editUser(this.editedItemIndex, new User(name));
  }

  onAdd() {
    const name = (<HTMLInputElement>document.getElementById('name')).value;
    this.slService.addUser(new User(name));
  }

  ngOnDestroy() {
    this.subscription.unsubscribe();
  }

}
